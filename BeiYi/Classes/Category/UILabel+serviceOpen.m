#import "UILabel+serviceOpen.h"

@implementation UILabel (serviceOpen)

- (UILabel *)addLabelWithFrame:(CGRect)frame text:(NSString *)text textAligment:(NSTextAlignment)textAligment {
    
    UILabel *label = [[UILabel alloc] initWithFrame:frame];
    label.text = text;
    label.textAlignment = textAligment;
    label.font = [UIFont systemFontOfSize:12];
    return label;
    
}

@end
